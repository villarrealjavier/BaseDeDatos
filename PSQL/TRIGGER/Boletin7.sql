--EJERCICIO 4
TRIGGER CASOS 
INSTEAD OF INSERT OR DELETE OR UPDATE ON SEDE_DEPARTAMENTOS
BEGIN 
	IF INSERTING THEN 
		INSERT INTO CENTROS VALUES (:NEW.NUMCE,:NEW.NOMCE, :NEW.DIRCE);
	
		INSERT INTO DEPARTAMENTOS(NUMDE,NOMDE,PRESU,DIREC,TIDIR,DEPDE) 
		VALUES (:NEW.NUMDE,:NEW.NOMDE,:NEW.PRESU,:NEW.DIREC,:NEW.TIDIR,:NEW.DEPDE);
	ELSIF UPDATING THEN
		UPDATE CENTROS
		SET NUMCE=:NEW.NUMCE,NOMCE=:NEW.NOMCE, DIRCE=:NEW.DIRCE
		WHERE NUMCE=:OLD.NUMCE AND NOMCE=:OLD.NOMCE AND DIRCE=:OLD.DIRCE;
	
		UPDATE DEPARTAMENTOS 
		SET NUMDE=:NEW.NUMDE , NOMDE=:NEW.NOMDE , PRESU=:NEW.PRESU , DIREC=:NEW.DIREC , TIDIR=:NEW.TIDIR , DEPDE=:NEW.DEPDE 
		WHERE NUMDE=:OLD.NUMDE AND NOMDE=:OLD.NOMDE AND PRESU=:OLD.PRESU AND DIREC=:OLD.DIREC AND TIDIR=:OLD.TIDIR AND DEPDE=:OLD.DEPDE ;
	
	ELSIF DELETING THEN
		DELETE FROM CENTROS
		WHERE NUMCE=:OLD.NUMCE AND NOMCE=:OLD.NOMCE AND DIRCE=:OLD.DIRCE;
	
		DELETE FROM DEPARTAMENTOS
		WHERE NUMDE=:OLD.NUMDE AND NOMDE=:OLD.NOMDE AND PRESU=:OLD.PRESU AND DIREC=:OLD.DIREC AND TIDIR=:OLD.TIDIR AND DEPDE=:OLD.DEPDE;
	END IF;
END CASOS;

