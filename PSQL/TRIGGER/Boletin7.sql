-- CENTROS
CREATE TABLE CENTROS (
    NUMCE   NUMBER(4)   PRIMARY KEY,
    NOMCE   VARCHAR2(30),
    DIRCE   VARCHAR2(30)
);


-- DEPARTAMENTOS
CREATE TABLE DEPARTAMENTOS (
    NUMDE   NUMBER(3)   PRIMARY KEY,
    NUMCE   NUMBER(4) REFERENCES CENTROS(NUMCE),
    DIREC   NUMBER(3),
    TIDIR   CHAR(1),
    PRESU   NUMBER(3,1),
    DEPDE   NUMBER(3) REFERENCES DEPARTAMENTOS(NUMDE),
    NOMDE   VARCHAR2(30)
);


