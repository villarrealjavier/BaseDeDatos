CREATE OR REPLACE PROCEDURE EMPLEADOS_SALARIO
AS 
	Numero_Empleados NUMBER(20);
	Salario_maximo NUMBER(20);
	Salario_minimo NUMBER(20);
	Salario_medio NUMBER(20);
BEGIN

	SELECT COUNT(NOMEM) AS CUENTA, MAX(SALAR) AS SALARIO_MAXIMO, MIN(SALAR) AS SALARIO_MINIMO, AVG(SALAR) AS SALARIO_MEDIO 
	INTO Numero_Empleados, Salario_maximo, Salario_minimo, Salario_medio
	FROM EMPLEADOS
	WHERE NUMDE=(SELECT NUMDE FROM DEPARTAMENTOS d WHERE NOMDE LIKE 'FINANZAS');
	
	DBMS_OUTPUT.PUT_LINE('Numero empleados: '|| Numero_Empleados);
	DBMS_OUTPUT.PUT_LINE('Salario Maximo empleados: '|| Salario_maximo);
	DBMS_OUTPUT.PUT_LINE('Salario Medio empleados: '|| Salario_minimo);
	DBMS_OUTPUT.PUT_LINE('Salario medio empleados: '|| Salario_medio);
	

END;

BEGIN 
	EMPLEADOS_SALARIO();
END;

--EJERCICIO2
--Escribe un procedimiento que suba un 10% el salario a los EMPLEADOS
--con más de 2 hijos y que ganen menos de 2000 €. Para cada empleado se
--mostrar por pantalla el código de empleado, nombre, salario anterior y final.
--Utiliza un cursor explícito. La transacción no puede quedarse a medias. Si
--por cualquier razón no es posible actualizar todos estos salarios, debe
--deshacerse el trabajo a la situación inicial.

CREATE OR REPLACE PROCEDURE EMPLEADOS.SUBIR_SALARIO
AS 
	SALARIO_NUEVO NUMBER;
	CURSOR SUBIDA IS
	SELECT DISTINCT NUMEM,NOMEM,SALAR,NUMHI,SALAR+(SALAR*0.10) AS SALARIO_FINAL FROM EMPLEADOS
	WHERE SALAR<2000 AND NUMHI>2;

BEGIN 
	FOR registro IN SUBIDA
	LOOP
		DBMS_OUTPUT.PUT_LINE('Numero de empleado: ' || registro.NUMEM ||
							' Nombre: ' || registro.NOMEM ||
							' Salario: ' || registro.SALAR ||
							' Numero de hijos: ' || registro.NUMHI ||
							' Salario nuevo: ' || registro.SALARIO_FINAL);
		UPDATE EMPLEADOS 
		SET SALAR= SALAR+(SALAR*0.10)
		WHERE NUMEM=registro.NUMEM
		AND NOMEM=registro.NOMEM
		AND SALAR=registro.SALAR
		AND NUMHI=registro.NUMHI;
	
	END LOOP;
			COMMIT;
	EXCEPTION
	
		
	WHEN OTHERS THEN 
		DBMS_OUTPUT.PUT_LINE('Se ha producido un error');
		ROLLBACK;
	
END;

BEGIN
	SUBIR_SALARIO();
END;


